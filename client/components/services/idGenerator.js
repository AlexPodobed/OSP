angular.module('ospApp')
  .factory("GeneratorID", [function () {
    function s4() {
      return Math.floor((1 + Math.random()) * 0x10000)
        .toString(16)
        .substring(1);
    }
    function generateUniqID(){
      return s4() + s4() + '-' + s4() + '-' + s4() + '-' +
        s4() + '-' + s4() + s4() + s4();
    }

    return {
      generate: generateUniqID
    }
  }]);
